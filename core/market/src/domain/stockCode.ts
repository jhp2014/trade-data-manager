// 표준 종목코드 불변식 — 내부 도메인의 종목코드는 항상 6자리 대문자 영숫자.
// 대부분 숫자(예: "005930")지만 KRX 숫자고갈 영숫자 코드("0007C0"·"0009K0")가 실존하므로
// 숫자만으로 좁히면 안 된다(시트 어댑터 테스트가 보존을 고정). 외부 표현(A접두·_AL 접미·
// 엑셀 앞0 손실)의 **정규화**는 그 표현이 합법적으로 존재하는 ingestion 경계(infra/broker
// 시트 어댑터)의 지식이고, 여기는 "정규화가 끝난 표준형인가"의 판정만 소유한다
// (HTTP 예외·변환 모름). api 경계는 이 규칙으로 검증만 한다(보정 없이 400).
export function isCanonicalStockCode(value: string): boolean {
    return /^[0-9A-Z]{6}$/.test(value);
}
